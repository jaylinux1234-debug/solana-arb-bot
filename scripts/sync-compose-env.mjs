function parseEnv(text) {
  const out = new Map();
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq < 1) continue;
    const key = trimmed.slice(0, eq).trim();
    const value = trimmed.slice(eq + 1).trim();
    if (SYNC_KEYS.has(key)) out.set(key, value);
  }
  return out;
}

if (!fs.existsSync(SOURCE)) {
  throw new Error("Missing .env — copy from .env.example and fill in values.");
}

const parsed = parseEnv(fs.readFileSync(SOURCE, "utf8"));

const lines = [
  "# Auto-generated by npm run sync:compose-env — do not edit by hand.",
  "# Non-secret compose vars only (secrets: secrets/encrypted/*.enc.yaml + Docker mounts).",
  `# Source: .env @ ${new Date().toISOString()}`,
  "",
];

/** Keys where empty string is meaningful (e.g. SOL-only: no midcaps). */
const ALLOW_EMPTY = new Set(["CEX_MIDCAPS"]);

for (const key of [...SYNC_KEYS]) {
  if (!parsed.has(key)) continue;
  const value = parsed.get(key);
  if (value || ALLOW_EMPTY.has(key)) lines.push(`${key}=${value ?? ""}`);
}

if (lines.length <= 3) {
  throw new Error("No compose keys found in .env (APP_ENV, SIGNER_TYPE, RPC vars, etc.)");
}

fs.writeFileSync(TARGET, `${lines.join("\n")}\n`, "utf8");
console.log(`Wrote ${TARGET} (${parsed.size} keys from .env)`);
